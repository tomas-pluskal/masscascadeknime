package uk.ac.ebi.masscascade.knime.utilities.massfilter;

import org.knime.core.data.DoubleValue;
import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

import uk.ac.ebi.masscascade.knime.datatypes.featurecell.FeatureValue;
import uk.ac.ebi.masscascade.knime.defaults.DefaultDialog;
import uk.ac.ebi.masscascade.parameters.Parameter;

/**
 * <code>NodeFactory</code> for the "MassTraceFilter" Node. Removes a list of masses from the peak collection within a
 * given tolerance intervall.
 * 
 * @author Stephan Beisken
 */
public class MassTraceFilterNodeFactory extends NodeFactory<MassTraceFilterNodeModel> {

	/**
	 * {@inheritDoc}
	 */
	@Override
	public MassTraceFilterNodeModel createNodeModel() {
		return new MassTraceFilterNodeModel();
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public int getNrNodeViews() {
		return 0;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeView<MassTraceFilterNodeModel> createNodeView(final int viewIndex,
			final MassTraceFilterNodeModel nodeModel) {
		return null;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public boolean hasDialog() {
		return true;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeDialogPane createNodeDialogPane() {

		DefaultDialog dialog = new DefaultDialog();

		dialog.addColumnSelection(Parameter.FEATURE_COLUMN, FeatureValue.class);
		dialog.addColumnSelection(Parameter.VALUE_COLUMN, 1, DoubleValue.class);
		dialog.addTextOption(Parameter.MZ_WINDOW_PPM, 5);

		return dialog.build();
	}
}
